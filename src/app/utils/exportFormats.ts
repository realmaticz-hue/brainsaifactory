// =============================================================================
// CSV EXPORT (spreadsheet compatible)
// =============================================================================

export function exportToCSV(posts: BlogPost[]): string {
  const headers = [
    'Title',
    'Content',
    'Duration (s)',
    'Quality Score',
    'Word Count',
    'Meta Description',
    'Slug',
    'Primary Keyword',
    'Secondary Keywords',
    'Character',
    'Date',
    'Angle',
  ].join(',');

  const escapeCSV = (value: any): string => {
    if (value === null || value === undefined) return '';
    const str = String(value);
    // Escape quotes and wrap in quotes if contains comma, quote, or newline
    if (str.includes(',') || str.includes('"') || str.includes('\n')) {
      return `"${str.replace(/"/g, '""')}"`;
    }
    return str;
  };

  const rows = posts.map(post => [
    escapeCSV(post.seoTitle || 'Untitled'),
    escapeCSV(post.content),
    escapeCSV(post.duration),
    escapeCSV(post.qualityScore || ''),
    escapeCSV(post.wordCount || ''),
    escapeCSV(post.metaDescription || ''),
    escapeCSV(post.slug || ''),
    escapeCSV(post.primaryKeyword || ''),
    escapeCSV((post.secondaryKeywords || []).join('; ')),
    escapeCSV(post.character?.name || ''),
    escapeCSV(new Date(post.timestamp).toISOString()),
    escapeCSV(post.angleLabel || ''),
  ].join(','));

  return [headers, ...rows].join('\n');
}

export function downloadCSV(posts: BlogPost[], filename?: string): void {
  const csv = exportToCSV(posts);
  const blob = new Blob([csv], { type: 'text/csv' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename || `blog-posts-${Date.now()}.csv`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
