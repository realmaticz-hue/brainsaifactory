import type { BlogPost } from './blogGenerator';

// =============================================================================
// MARKDOWN EXPORT
// =============================================================================

export function exportToMarkdown(posts: BlogPost[]): string {
  const markdown = posts.map(post => {
    const frontmatter = `---
title: ${post.seoTitle || 'Untitled'}
duration: ${post.duration}s
quality-score: ${post.qualityScore || 'N/A'}
word-count: ${post.wordCount || 0}
primary-keyword: ${post.primaryKeyword || 'N/A'}
secondary-keywords: ${(post.secondaryKeywords || []).join(', ')}
character: ${post.character?.name || 'N/A'}
date: ${new Date(post.timestamp).toISOString()}
---

`;

    const content = `# ${post.seoTitle || 'Untitled Blog Post'}

${post.metaDescription ? `> ${post.metaDescription}\n\n` : ''}${post.content}

---

**Metadata:**
- Duration: ${post.duration} seconds
- Quality Score: ${post.qualityScore || 'N/A'}
- Word Count: ${post.wordCount || 0}
- Primary Keyword: ${post.primaryKeyword || 'N/A'}
${post.secondaryKeywords && post.secondaryKeywords.length > 0 ? `- Secondary Keywords: ${post.secondaryKeywords.join(', ')}\n` : ''}${post.character ? `- Character: ${post.character.name}\n` : ''}
`;

    return frontmatter + content;
  }).join('\n\n---\n\n');

  return markdown;
}

export function downloadMarkdown(posts: BlogPost[], filename?: string): void {
  const markdown = exportToMarkdown(posts);
  const blob = new Blob([markdown], { type: 'text/markdown' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename || `blog-posts-${Date.now()}.md`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

